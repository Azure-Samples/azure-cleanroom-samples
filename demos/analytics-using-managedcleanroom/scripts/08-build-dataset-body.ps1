<#
.SYNOPSIS
    Builds dataset publish JSON bodies from metadata generated by prior steps.

.DESCRIPTION
    Reads datastore metadata (from 05-prepare-data.ps1) and identity metadata
    (from 06-setup-oidc-storage.ps1) to generate ready-to-publish dataset JSON
    files. These files can be passed to `az managedcleanroom frontend analytics
    dataset publish --body @file` or to Invoke-RestMethod.

    For Northwind: generates 1 input dataset body.
    For Woodgrove: generates 1 input + 1 output dataset body.

    No frontend calls are made.

    Prerequisites:
    - 05-prepare-data.ps1 (generates datastore metadata)
    - 06-setup-oidc-storage.ps1 (generates identity metadata + issuer URL)
    - 08-prepare-dataset-keys.ps1 (CPK only — generates KEKs, updates metadata)
#>
param(
    [Parameter(Mandatory)]
    [string]$resourceGroup,

    [Parameter(Mandatory)]
    [ValidateSet("northwind", "woodgrove", IgnoreCase = $false)]
    [string]$persona,

    [string]$maaUrl = "https://sharedeus.eus.attest.azure.net",

    [string]$outDir = "./generated"
)

$ErrorActionPreference = 'Stop'

# Resolve outDir to absolute path
$outDir = $ExecutionContext.SessionState.Path.GetUnresolvedProviderPathFromPSPath($outDir)

# -- Load metadata ----------------------------------------------------------------
$datastoreMetadataFile = Join-Path $outDir "datastores" "$persona-datastore-metadata.json"
if (-not (Test-Path $datastoreMetadataFile)) {
    Write-Host "ERROR: '$datastoreMetadataFile' not found." -ForegroundColor Red
    exit 1
}
$datastoreMeta = Get-Content $datastoreMetadataFile -Raw | ConvertFrom-Json

$identityMetadataFile = Join-Path $outDir $resourceGroup "identity-metadata.json"
if (-not (Test-Path $identityMetadataFile)) {
    Write-Host "ERROR: '$identityMetadataFile' not found." -ForegroundColor Red
    exit 1
}
$identityMeta = Get-Content $identityMetadataFile -Raw | ConvertFrom-Json

$issuerUrlFile = Join-Path $outDir $resourceGroup "issuer-url.txt"
if (-not (Test-Path $issuerUrlFile)) {
    Write-Host "ERROR: '$issuerUrlFile' not found." -ForegroundColor Red
    exit 1
}
$oidcIssuerUrl = (Get-Content $issuerUrlFile -Raw).Trim()

$isCpk = ($datastoreMeta.input.encryptionMode -eq "CPK")
$encryptionMode = if ($isCpk) { "CPK" } else { "SSE" }

# -- Build dataset body -----------------------------------------------------------
function New-DatasetPublishBody {
    param(
        [PSCustomObject]$Meta,
        [PSCustomObject]$Identity,
        [string]$IssuerUrl,
        [string]$AccessMode,
        [string[]]$AllowedFields,
        [string]$EncMode,
        [string]$Maa
    )

    $body = [ordered]@{
        name = $Meta.name
        datasetSchema = [ordered]@{
            format = $Meta.schema.format
            fields = @($Meta.schema.fields | ForEach-Object {
                [ordered]@{ fieldName = $_.fieldName; fieldType = $_.fieldType }
            })
        }
        datasetAccessPolicy = [ordered]@{
            accessMode = $AccessMode
        }
        store = [ordered]@{
            storageAccountUrl  = $Meta.storeUrl
            containerName      = $Meta.containerName
            storageAccountType = $Meta.storeType
            encryptionMode     = $EncMode
        }
        identity = [ordered]@{
            name      = $Identity.identityName
            clientId  = $Identity.clientId
            tenantId  = $Identity.tenantId
            issuerUrl = $IssuerUrl
        }
    }

    if ($EncMode -eq "CPK" -and $Meta.encryption) {
        $body.dek = [ordered]@{
            keyVaultUrl = $Meta.encryption.dekStoreUrl
            secretId    = $Meta.encryption.dekSecretName
        }
        $body.kek = [ordered]@{
            keyVaultUrl = $Meta.encryption.kekStoreUrl
            secretId    = $Meta.encryption.kekName
            maaUrl      = $Maa
        }
    }

    if ($AllowedFields -and $AllowedFields.Count -gt 0) {
        $body.datasetAccessPolicy.allowedFields = @($AllowedFields)
    }

    return $body
}

# -- Generate JSON files ----------------------------------------------------------
$publishDir = Join-Path $outDir "publish"
New-Item -ItemType Directory -Path $publishDir -Force | Out-Null

$inputMeta = $datastoreMeta.input

# Input dataset
$inputAllowedFields = if ($persona -eq "northwind") {
    @("hashed_email", "annual_income", "region")
} else {
    @("user_id", "hashed_email", "purchase_history")
}

$inputBody = New-DatasetPublishBody -Meta $inputMeta -Identity $identityMeta `
    -IssuerUrl $oidcIssuerUrl -AccessMode "read" -AllowedFields $inputAllowedFields `
    -EncMode $encryptionMode -Maa $maaUrl

$inputFile = Join-Path $publishDir "$persona-input-dataset.json"
$inputBody | ConvertTo-Json -Depth 20 | Out-File -FilePath $inputFile -Encoding utf8
Write-Host "Input dataset body: $inputFile" -ForegroundColor Green

# Output dataset (Woodgrove only)
if ($persona -eq "woodgrove" -and $datastoreMeta.output) {
    $outputBody = New-DatasetPublishBody -Meta $datastoreMeta.output -Identity $identityMeta `
        -IssuerUrl $oidcIssuerUrl -AccessMode "write" -AllowedFields @("user_id") `
        -EncMode $encryptionMode -Maa $maaUrl

    $outputFile = Join-Path $publishDir "woodgrove-output-dataset.json"
    $outputBody | ConvertTo-Json -Depth 20 | Out-File -FilePath $outputFile -Encoding utf8
    Write-Host "Output dataset body: $outputFile" -ForegroundColor Green
}

Write-Host "Dataset body generation complete ($encryptionMode)." -ForegroundColor Green
